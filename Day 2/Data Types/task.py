# Subscripting- printing a specific character in a string (starts at 0 and can work backwards at -1)
print("Hello"[4])
print("Hello"[-1])

#String
print ("123" + "345")

#Integer = whole number
print (123 + 345)

#Large integers
print (123_445_789)

#Float =  floating point number
print (3.1455)

#Boolean
print(True)
print(False)
